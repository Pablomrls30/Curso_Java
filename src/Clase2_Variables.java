public class Clase2_Variables {
	public static void main(String args[])
	{
		int miVariableEntera = 10;
		System.out.println(miVariableEntera);
		
		miVariableEntera = 5; //Una vez definida una variable ya no es necesario poner que tipo de dato es
		System.out.println(miVariableEntera); //Escribe syso + Ctrl + Espacio para poder imprimir una cadena 
		
		String miVariableCadena = "Saludos"; //Variable tipo string
		System.out.println(miVariableCadena);
		
		miVariableCadena = "Adios";
		System.out.println(miVariableCadena);
		
		
	}
}
