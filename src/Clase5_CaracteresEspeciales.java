public class Clase5_CaracteresEspeciales {
	public static void main(String[] args) {
		var nombre = "Pablo";
		System.out.println("Nueva linea: \n" + nombre);//Esto har√° un salto de linea
		System.out.println("Tabuladro: \t" + nombre);//Hace un tabulador
		System.out.println("Retroceso: \b\b" + nombre);//Crea un retroceso
		System.out.println("Comilla simple: \'" + nombre + "\'");//Crea la comilla simple
		System.out.println("Comilla doble: \"" + nombre + "\"");//Crea la comilla doble
	}
}
